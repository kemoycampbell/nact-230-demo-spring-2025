import turtle

#start by setting the turtle to face up
turtle.left(90)
#draw the top of the sqare
turtle.forward(100)
turtle.right(90)
#draw the right side of the square
turtle.forward(100)
turtle.right(90)
#draw the bottom side of the square
turtle.forward(100)
turtle.right(90)
#draw the left side of the square
turtle.forward(100)
turtle.right(90)

#keep the windows open
turtle.done()